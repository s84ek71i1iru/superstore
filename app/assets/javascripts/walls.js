function draw() {

	var wall = $('#wall');

	wall.append('<div class="div_table"></div>');

	var canvas = wall.find('.div_table');

	$(sticker).each(function(index, value){
		var _x = value[0],
				_y = value[1],
				_color = value[2],
				_current_tr_length = wall.find('.div_table_tr').size();

		if (_y > _current_tr_length) {
			for (var i = _current_tr_length; i <= _y; i++) {
				canvas.append('<div class="div_table_tr"></div>');
			}
		}

		var _current_tr = wall.find('.div_table_tr').eq(_y - 1);
				_current_td_length = _current_tr.find('.div_table_td').size();

		if (_x > _current_td_length) {
			for (var j = _current_td_length; j <= _x; j++) {
				_current_tr.append('<div class="div_table_td"></div>');
			}
		}

		_current_tr.find('.div_table_td').eq(_x - 1).css('background', '#' + _color);
	});
}